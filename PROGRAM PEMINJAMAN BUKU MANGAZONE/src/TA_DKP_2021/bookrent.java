                lblCaraBayar.setText(rdbGopay.getActionCommand());
            if(rdbDebit.isSelected())
                lblCaraBayar.setText(rdbDebit.getActionCommand());
            if(rdbCash.isSelected())
                lblCaraBayar.setText(rdbCash.getActionCommand());
