            int nShingekinoKyojin = Integer.parseInt(txtShingekinoKyojin.getText());
            int nBNHA = Integer.parseInt(txtBNHA.getText());
            int nKemonoJihen = Integer.parseInt(txtKemonoJihen.getText());
            int nHaikyuu = Integer.parseInt(txtHaikyuu.getText());
            int nGakuenBabysitter = Integer.parseInt(txtGakuenBabysitter.getText());
